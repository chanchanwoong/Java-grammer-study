package com.javastudy.chapter7;

public class Modifier02 {
    /**
     * static
     *  공통적인 의미를 가지고 있다.
     *  인스턴스 멤버를 사용하지 않는 메서드는 static 메서드로 선언한는 것이 인스턴스 생성안하고 호출 가능해서 편리하고 속도도 빠름
     *  클래스 변수 같은 경우에 인스턴스에 관계없이 같은 값을 공유한다.
     *      인스턴스 변수는 객체마다 독립적인 값을 가진다.
     *  인스턴스를 생성하지 않고도 사용할 수 있다.
     *      그 이유는, 클래스가 메모리에 로드될 때 생성되기 때문이다.
     *
     *
     *  static 사용가능 한 곳
     *      멤버변수, 메서드, 초기화 블럭
     */
}
